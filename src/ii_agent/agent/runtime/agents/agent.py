@dataclass
class IIAgent:
    user_id: str
    session_id: str
    model: Model
    name: str = None

    _internal_lock = asyncio.Lock()
    # Agent UUID (autogenerated if not set)
    id: Optional[str] = None

    session_store: Optional[SessionStore] = None
    # Default session state (stored in the database to persist across runs)
    session_state: Optional[Dict[str, Any]] = None

    # Session summary manager
    session_summary_manager: Optional[SessionSummaryManager] = None

    # --- Agent Tools ---
    tools: Optional[List[Union[BaseAgentTool, Toolkit, Callable, Function, Dict]]] = None
    tool_call_limit: Optional[int] = None
    tool_choice: Optional[Union[str, Dict[str, Any]]] = None
    tool_hooks: Optional[List[Callable]] = None

    # --- Agent Hooks ---
    pre_hooks: Optional[List[Union[Callable[..., Any]]]] = None
    post_hooks: Optional[List[Union[Callable[..., Any]]]] = None

    # Provide the system message as a string or function
    system_message: Optional[Union[str, Callable, Message]] = None
    description: Optional[str] = None
    instructions: Optional[Union[str, List[str], Callable]] = None
    additional_context: Optional[str] = None

    # --- Agent Response Settings ---
    retries: int = 0
    delay_between_retries: int = 1
    exponential_backoff: bool = False

    # --- Agent Streaming ---
    stream: Optional[bool] = None
    stream_events: Optional[bool] = None

    # Persist the events on the run response
    store_events: bool = False
    events_to_skip: Optional[List[RunEvent]] = None

    # Metadata stored with this agent
    metadata: Optional[Dict[str, Any]] = None

    # --- Sub-Agent Support ---
    sub_agents: Optional[List["IIAgent"]] = None
    delegate_to_all_members: bool = False
    stream_member_events: bool = True
    store_member_responses: bool = False
    role: Optional[str] = None

    def __post_init__(self) -> None:
        # Ensure tools is a list
        if self.tools is not None:
            self.tools = list(self.tools)
        else:
            self.tools = []

        # Default events_to_skip
        if self.events_to_skip is None:
            self.events_to_skip = [
                RunEvent.run_content_delta,
                RunEvent.reasoning_delta,
            ]
        if self.session_store is None:
            self.session_store = NoOpSessionStore()
        if self.session_summary_manager is None:
            self._set_session_summary_manager()
        # Initialize sub-agents
        if self.sub_agents is None:
            self.sub_agents = []

        # --- Create collaborators ---
        self._message_builder = MessageBuilder(
            model=self.model,
            system_message=self.system_message,
        )
        self._tool_manager = ToolManager(model=self.model)
        self._response_handler = ResponseHandler(model=self.model)
        self._hook_executor = HookExecutor()
        container = ServiceContainer.create()
        self._sandbox_provider = SandboxProvider(
            session_id=self.session_id,
            user_id=self.user_id,
            lock=self._internal_lock,
            container=container,
        )
        self._hitl_handler = HITLHandler(
            model=self.model,
            tool_manager=self._tool_manager,
        )
        self._delegation_manager = DelegationManager(
            session_store=self.session_store,
        )

        # Initialize sub-agents with shared context
        for sub_agent in self.sub_agents:
            self._delegation_manager.initialize_sub_agent(sub_agent)

    # ========================================
    # Public API
    # ========================================

    def set_id(self) -> None:
        if self.id is None:
            self.id = generate_id_from_name(self.name)

    @property
    def should_persist(self) -> bool:
        return self.session_store is not None

    def add_tool(self, tool: Union[Toolkit, Callable, Function, Dict, BaseAgentTool]):
        if not self.tools:
            self.tools = []
        self.tools.append(tool)

    def set_tools(self, tools: Sequence[Union[Toolkit, Callable, Function, Dict, BaseAgentTool]]):
        self.tools = list(tools) if tools else []

    def add_sub_agent(self, agent: "IIAgent") -> None:
        if self.sub_agents is None:
            self.sub_agents = []
        self._delegation_manager.initialize_sub_agent(agent)
        self.sub_agents.append(agent)

    @property
    def sandbox(self) -> Optional[SandboxManager]:
        return self._sandbox_provider.sandbox

    @sandbox.setter
    def sandbox(self, value: Optional[SandboxManager]) -> None:
        self._sandbox_provider.sandbox = value

    async def init_sandbox(self) -> SandboxManager:
        return await self._sandbox_provider.init_sandbox()

    @staticmethod
    def cancel_run(run_id: str) -> bool:
        return cancel_run_global(run_id)

    def as_tool(
        self,
        name: str = "sub_agent",
        description: str = "A sub-agent that can perform tasks",
        input_schema: Optional[Dict[str, Any]] = None,
        read_only: bool = True,
    ) -> "BaseAgentTool":
        return AgentAsTool(agent_instance=self, name=name)

