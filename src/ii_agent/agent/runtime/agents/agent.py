    async def arun(  # type: ignore
        self,
        input: Union[str, List, Dict, Message, BaseModel, List[Message]],
        *,
        run_id: Optional[str] = None,
        stream: Optional[bool] = None,
        user_id: Optional[str] = None,
        session_id: Optional[str] = None,
        session_state: Optional[Dict[str, Any]] = None,
        run_context: Optional[RunContext] = None,
        audio: Optional[Sequence[Audio]] = None,
        images: Optional[Sequence[Image]] = None,
        videos: Optional[Sequence[Video]] = None,
        files: Optional[Sequence[File]] = None,
        stream_events: Optional[bool] = None,
        metadata: Optional[Dict[str, Any]] = None,
        debug_mode: Optional[bool] = None,
        is_sub_agent: bool = False,
        **kwargs: Any,
    ) -> Union[RunOutput, AsyncIterator[RunOutputEvent]]:
        """Async Run the Agent and return the response."""
        run_id = run_id or str(uuid4())
        if not is_sub_agent:
            await register_run(run_id)

        background_tasks = kwargs.pop("background_tasks", None)
        if background_tasks is not None:
            from fastapi import BackgroundTasks
            background_tasks: BackgroundTasks = background_tasks  # type: ignore

        validated_input = self._validate_input(input)
        session_id, user_id = self._initialize_session(session_id=session_id, user_id=user_id)

        image_artifacts, video_artifacts, audio_artifacts, file_artifacts = (
            validate_media_object_id(images=images, videos=videos, audios=audio, files=files)
        )

        run_input = RunInput(
            input_content=validated_input,
            images=image_artifacts,
            videos=video_artifacts,
            audios=audio_artifacts,
            files=files,
        )

        if stream is None:
            stream = False if self.stream is None else self.stream
        if stream is False:
            stream_events = False
        if stream_events is None:
            stream_events = False if self.stream_events is None else self.stream_events

        self.stream = self.stream or stream
        self.stream_events = self.stream_events or stream_events

        self.model = cast(Model, self.model)

        if self.metadata is not None:
            if metadata is None:
                metadata = self.metadata
            else:
                merge_dictionaries(metadata, self.metadata)

        run_context = run_context or RunContext(
            run_id=run_id,
            session_id=session_id,
            user_id=user_id,
            session_state=session_state,
            metadata=metadata,
        )

        run_response = RunOutput(
            model=self.model.id,
            run_id=run_id,
            session_id=session_id,
            agent_id=self.id,
            user_id=user_id,
            agent_name=self.name,
            metadata=run_context.metadata,
            session_state=run_context.session_state,
            input=run_input,
        )

        run_response.model = self.model.id if self.model is not None else None
        run_response.model_provider = self.model.provider if self.model is not None else None

        if is_sub_agent:
            run_response.parent_run_id = run_id

        run_response.metrics = Metrics()
        run_response.metrics.start_timer()

        num_attempts = self.retries + 1

        for attempt in range(num_attempts):
            if attempt > 0:
                logger.debug(
                    f"Retrying Agent run {run_id}. Attempt {attempt + 1} of {num_attempts}..."
                )
            try:
                if run_input.contains_media():
                    sandbox = await self.init_sandbox()
                    await sandbox.create_directory(sandbox.upload_path, exist_ok=True)
                    sandbox_files, sandbox_images = await self._sandbox_provider.upload_media(
                        sandbox, run_input, sandbox.upload_path
                    )
                    if sandbox_files:
                        run_input.files = sandbox_files
                    if sandbox_images:
                        run_input.images = sandbox_images
                    run_response.input = run_input

                if stream:
                    return self._arun_stream(
                        run_response=run_response,
                        run_context=run_context,
                        user_id=user_id,
                        stream_events=stream_events,
                        session_id=session_id,
                        debug_mode=debug_mode,
                        background_tasks=background_tasks,
                        is_sub_agent=is_sub_agent,
                        **kwargs,
                    )
                else:
                    return await self._arun(
                        run_response=run_response,
                        run_context=run_context,
                        user_id=user_id,
                        session_id=session_id,
                        debug_mode=debug_mode,
                        background_tasks=background_tasks,
                        is_sub_agent=is_sub_agent,
                        **kwargs,
                    )
            except (InputCheckError, OutputCheckError) as e:
                logger.error(f"Validation failed: {str(e)} | Check trigger: {e.check_trigger}")
                raise e
            except KeyboardInterrupt:
                run_response.content = "Operation cancelled by user"
                run_response.status = RunStatus.ABORTED
                if stream:
                    return async_generator_wrapper(
                        create_run_cancelled_event(
                            from_run_response=run_response,
                            reason="Operation cancelled by user",
                        )
                    )
                else:
                    return run_response
            except Exception as e:
                if attempt < num_attempts - 1:
                    if self.exponential_backoff:
                        delay = self.delay_between_retries * (2**attempt)
                    else:
                        delay = self.delay_between_retries
                    logger.warning(
                        f"Attempt {attempt + 1}/{num_attempts} failed: {str(e)}. Retrying in {delay}s..."
                    )
                    await asyncio.sleep(delay)
                    continue
                else:
                    logger.error(f"All {num_attempts} attempts failed. Final error: {str(e)}")
                    if stream:
                        return async_generator_wrapper(
                            create_run_error_event(run_response, error=str(e))
                        )
                    raise e

        raise Exception(f"Failed after {num_attempts} attempts.")

