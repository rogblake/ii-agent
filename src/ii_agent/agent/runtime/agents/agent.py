    def _validate_input(
        self, input: Union[str, List, Dict, Message, BaseModel]
    ) -> Union[str, List, Dict, Message, BaseModel]:
        return input

    def _set_session_summary_manager(self) -> None:
        if self.session_summary_manager is None:
            self.session_summary_manager = SessionSummaryManager(model=self.model)
        if self.session_summary_manager is not None:
            if self.session_summary_manager.model is None:
                self.session_summary_manager.model = self.model

    def _initialize_session(
        self,
        session_id: Optional[str] = None,
        user_id: Optional[str] = None,
    ) -> Tuple[str, Optional[str]]:
        if session_id is None:
            if self.session_id:
                session_id = self.session_id
            else:
                session_id = str(uuid4())
                self.session_id = session_id
        logger.debug(f"Session ID: {session_id}", center=True)
        if user_id is None or user_id == "":
            user_id = self.user_id
        return session_id, user_id

    def _initialize_session_state(
        self,
        session_state: Dict[str, Any],
        user_id: Optional[str] = None,
        session_id: Optional[str] = None,
        run_id: Optional[str] = None,
    ) -> Dict[str, Any]:
        if user_id:
            session_state["current_user_id"] = user_id
        if session_id is not None:
            session_state["current_session_id"] = session_id
        if run_id is not None:
            session_state["current_run_id"] = run_id
        return session_state

    async def _aread_or_create_session(
        self,
        *,
        session_id: str,
        user_id: Optional[str] = None,
    ) -> AgentSession:
        if self.should_persist and user_id:
            try:
                from ii_agent.core.exceptions import NotFoundError
                agent_session = await self.session_store.get_session(
                    session_id=session_id, user_id=user_id
                )
                logger.debug(f"Loaded existing AgentSession from store: {session_id}")
                return agent_session
            except NotFoundError:
                logger.debug(f"Session {session_id} not found in store, creating new one")
            except Exception as e:
                logger.warning(f"Error loading session from store: {e}, creating new one")

        logger.debug(f"Creating new AgentSession: {session_id}")
        session_data = {}
        if self.session_state is not None:
            from copy import deepcopy
            session_data["session_state"] = deepcopy(self.session_state)

        agent_session = AgentSession(
            session_id=session_id,
            user_id=user_id or self.user_id,
            agent_id=self.id,
            agent_data=self._message_builder.get_agent_data(self.name, self.id, self.model),
            session_data=session_data,
            metadata=self.metadata,
            created_at=int(time.time()),
        )
        return agent_session

    async def _acleanup_and_store(
        self,
        run_response: RunOutput,
        session: AgentSession,
        run_context: Optional[RunContext] = None,
    ) -> None:
        if run_response.metrics:
            run_response.metrics.stop_timer()

        if (
            session.session_data is not None
            and run_context is not None
            and run_context.session_state is not None
        ):
            run_response.session_state = run_context.session_state

        session.add_run(run=run_response)

        if self.should_persist and not run_response.is_sub_agent_response:
            try:
                await self.session_store.save_run(run=run_response)
                logger.info(
                    f"Saved run {run_response.run_id} to session {run_response.session_id} "
                    f"with status {run_response.status if run_response.status else 'unknown'}"
                )
            except Exception as e:
                logger.error(f"Error saving run to store: {e}")
                raise

        if run_context is not None and run_context.session_state is not None:
            if session.session_data is not None:
                session.session_data["session_state"] = run_context.session_state
            else:
                session.session_data = {"session_state": run_context.session_state}

    def _get_delegate_func(
        self,
        run_response: RunOutput,
        run_context: RunContext,
        session: AgentSession,
        user_id: Optional[str],
        stream: bool,
        stream_events: bool,
    ) -> Optional[Function]:
        """Create delegation function if sub-agents exist."""
        if not self.sub_agents:
            return None
        return self._delegation_manager.get_delegate_task_function(
            sub_agents=self.sub_agents,
            run_response=run_response,
            run_context=run_context,
            session=session,
            parent_agent=self,
            user_id=user_id,
            stream=stream,
            stream_events=stream_events,
            delegate_to_all_members=self.delegate_to_all_members,
            stream_member_events=self.stream_member_events,
            store_member_responses=self.store_member_responses,
        )

    async def _prepare_continue_run_response(
        self,
        run_id: str,
        run_response: Optional[RunOutput],
        updated_tools: Optional[List[ToolExecution]],
        requirements: Optional[List[RunRequirement]] = None,
        session_id: Optional[str] = None,
    ) -> RunOutput:
        """Load or validate the RunOutput for a continue-run."""
        if run_response is not None:
            return run_response

        if run_id is not None:
            if updated_tools is None and requirements is None:
                raise ValueError(
                    "Either updated tools or requirements are required to continue a run from a run_id."
                )

            run_response = await self.session_store.get_by_run_id(
                run_id=run_id, session_id=session_id
            )
            if run_response is None:
                raise RuntimeError(f"No runs found for run ID {run_id}")

            if updated_tools is not None:
                run_response.tools = updated_tools
            elif requirements is not None:
                run_response.requirements = requirements
                updated_tools = [
                    req.tool_execution for req in requirements if req.tool_execution is not None
                ]
                if updated_tools and run_response.tools:
                    updated_tools_map = {tool.tool_call_id: tool for tool in updated_tools}
                    run_response.tools = [
                        updated_tools_map.get(tool.tool_call_id, tool)
                        for tool in run_response.tools
                    ]
                else:
                    run_response.tools = updated_tools

            return run_response

        raise ValueError("Either run_response or run_id must be provided.")
