    def acontinue_run(  # type: ignore
        self,
        run_id: Optional[str] = None,
        *,
        run_response: Optional[RunOutput] = None,
        updated_tools: Optional[List[ToolExecution]] = None,
        stream: bool = True,
        stream_events: Optional[bool] = None,
        user_id: Optional[str] = None,
        session_id: Optional[str] = None,
        metadata: Optional[Dict[str, Any]] = None,
        debug_mode: Optional[bool] = None,
        **kwargs: Any,
    ) -> Union[RunOutput, AsyncIterator[Union[RunOutputEvent, RunOutput]]]:
        """Continue a paused run with updated tools/user inputs."""
        if run_id is None and run_response is None:
            raise ValueError("Either run_id or run_response must be provided")
        if run_id is not None and run_response is not None:
            raise ValueError("Only one of run_id or run_response should be provided, not both")

        background_tasks = kwargs.pop("background_tasks", None)
        if background_tasks is not None:
            from fastapi import BackgroundTasks
            background_tasks: BackgroundTasks = background_tasks  # type: ignore

        session_id, user_id = self._initialize_session(session_id=session_id, user_id=user_id)

        if stream is None:
            stream = False if self.stream is None else self.stream
        if stream is False:
            stream_events = False
        if stream_events is None:
            stream_events = False if self.stream_events is None else self.stream_events

        self.model = cast(Model, self.model)

        if self.metadata is not None:
            if metadata is None:
                metadata = self.metadata
            else:
                merge_dictionaries(metadata, self.metadata)

        if run_id is None and run_response is not None:
            run_id = run_response.run_id

        run_context = RunContext(
            run_id=run_id,
            session_id=session_id,
            user_id=user_id,
            session_state={},
            metadata=metadata,
        )

        if stream:
            return self._acontinue_run_stream(
                run_id=run_id,
                run_response=run_response,
                run_context=run_context,
                updated_tools=updated_tools,
                session_id=session_id,
                user_id=user_id,
                stream_events=stream_events,
                debug_mode=debug_mode,
                background_tasks=background_tasks,
                **kwargs,
            )
        else:
            return self._acontinue_run(
                run_id=run_id,
                run_response=run_response,
                run_context=run_context,
                updated_tools=updated_tools,
                session_id=session_id,
                user_id=user_id,
                debug_mode=debug_mode,
                background_tasks=background_tasks,
                **kwargs,
            )

    # ========================================
    # Private Orchestration
    # ========================================

    async def _arun(
        self,
        run_response: RunOutput,
        run_context: RunContext,
        session_id: str,
        user_id: Optional[str] = None,
        debug_mode: Optional[bool] = None,
        background_tasks: Optional[Any] = None,
        is_sub_agent: bool = False,
        **kwargs: Any,
    ) -> RunOutput:
        """Run the Agent (non-streaming)."""
        logger.debug(f"Agent Run Start: {run_response.run_id}")

        if self.should_persist and not is_sub_agent:
            try:
                await self.session_store.get_or_create_run_task(
                    session_id=session_id,
                    run_id=run_response.run_id,
                    status=RunStatus.RUNNING,
                )
            except Exception as e:
                logger.error(f"Failed to create run task: {e}")
                raise

        agent_session = await self._aread_or_create_session(
            session_id=session_id, user_id=user_id
        )

        run_context.session_state = self._initialize_session_state(
            session_state=run_context.session_state or {},
            user_id=user_id,
            session_id=session_id,
            run_id=run_response.run_id,
        )

        run_input = cast(RunInput, run_response.input)
        self.model = cast(Model, self.model)

        # Execute pre-hooks
        if self.pre_hooks is not None:
            async for _ in self._hook_executor.execute_pre_hooks(
                hooks=self.pre_hooks,
                run_response=run_response,
                run_context=run_context,
                run_input=run_input,
                session=agent_session,
                agent_ref=self,
                user_id=user_id,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
                debug_mode=debug_mode,
                background_tasks=background_tasks,
                **kwargs,
            ):
                pass

        # Determine tools
        processed_tools = await self._tool_manager.connect_and_get_tools(self.tools)
        delegate_func = self._get_delegate_func(run_response, run_context, agent_session, user_id, False, False)
        _tools = self._tool_manager.determine_tools_for_model(
            processed_tools=processed_tools,
            tool_hooks=self.tool_hooks,
            run_response=run_response,
            run_context=run_context,
            session=agent_session,
            stream=False,
            stream_events=False,
            user_id=user_id,
            agent_ref=self,
            delegate_func=delegate_func,
        )

        # Session summary
        if self.session_summary_manager is not None:
            if self.session_summary_manager.should_summary(agent_session):
                try:
                    session_summary = await self.session_summary_manager.acreate_session_summary(
                        session=agent_session
                    )
                    if session_summary is not None:
                        run_response.summary = session_summary
                except Exception as e:
                    logger.warning(f"Error in session summary creation: {str(e)}")

        if self.should_persist:
            self.session_store.save_run(run_response)

        # Prepare messages
        run_messages = await self._message_builder.get_run_messages(
            run_response=run_response,
            run_context=run_context,
            input=run_input.input_content,
            session=agent_session,
            agent_id=self.id,
            user_id=user_id,
            audio=run_input.audios,
            images=run_input.images,
            videos=run_input.videos,
            files=run_input.files,
            tools=_tools,
            **kwargs,
        )
        if len(run_messages.messages) == 0:
            logger.error("No messages to be sent to the model.")

        try:
            await raise_if_cancelled(run_response.run_id)

            model_response: ModelResponse = await self.model.aresponse(
                messages=run_messages.messages,
                tools=_tools,
                tool_choice=self.tool_choice,
                tool_call_limit=self.tool_call_limit,
                run_response=run_response,
            )

            await raise_if_cancelled(run_response.run_id)

            self._response_handler.update_run_response(
                model_response=model_response,
                run_response=run_response,
                run_messages=run_messages,
                run_context=run_context,
            )

            store_media_util(run_response, model_response)

            # Post-hooks
            if self.post_hooks is not None:
                async for _ in self._hook_executor.execute_post_hooks(
                    hooks=self.post_hooks,
                    run_output=run_response,
                    run_context=run_context,
                    session=agent_session,
                    agent_ref=self,
                    user_id=user_id,
                    events_to_skip=self.events_to_skip,
                    store_events=self.store_events,
                    debug_mode=debug_mode,
                    background_tasks=background_tasks,
                    **kwargs,
                ):
                    pass

            await raise_if_cancelled(run_response.run_id)

            # HITL check
            if any(tool_call.is_paused for tool_call in run_response.tools or []):
                return await self._hitl_handler.handle_paused(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                    cleanup_fn=self._acleanup_and_store,
                )

            run_response.status = RunStatus.COMPLETED
            logger.debug(f"Agent Run End: {run_response.run_id}")
            return run_response

        except RunCancelledException as e:
            logger.info(f"Run {run_response.run_id} was cancelled")
            run_response.status = RunStatus.ABORTED
            self._response_handler.add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_abort_message(str(e)),
                is_error=False,
            )
            self._response_handler.finalize_run_response(
                run_response=run_response, run_messages=run_messages,
            )
            if not run_response.content:
                run_response.content = str(e)
            return run_response

        except Exception as e:
            logger.error(f"Run {run_response.run_id} failed with error: {e}")
            tb = traceback.format_exc()
            logger.error(f"Traceback:\n{tb}")
            run_response.status = RunStatus.ERROR
            self._response_handler.add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_error_message(str(e)),
                is_error=True,
            )
            self._response_handler.finalize_run_response(
                run_response=run_response, run_messages=run_messages,
            )
            run_response.error_message = tb[:500]
            if not run_response.content:
                run_response.content = str(e)
            raise

        finally:
            await self._tool_manager.disconnect_all()
            if run_response.status != RunStatus.PAUSED:
                await self._acleanup_and_store(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                )
            if not is_sub_agent:
                await cleanup_run(run_response.run_id)

    async def _arun_stream(
        self,
        run_response: RunOutput,
        run_context: RunContext,
        session_id: str,
        user_id: Optional[str] = None,
        stream_events: bool = False,
        yield_run_output: Optional[bool] = None,
        debug_mode: Optional[bool] = None,
        background_tasks: Optional[Any] = None,
        is_sub_agent: bool = False,
        **kwargs: Any,
    ) -> AsyncIterator[Union[RunOutputEvent, RunOutput]]:
        """Run the Agent (streaming)."""
        logger.debug(f"Agent Run Start: {run_response.run_id}", center=True)

        if self.should_persist and not is_sub_agent:
            try:
                await self.session_store.get_or_create_run_task(
                    session_id=session_id,
                    run_id=run_response.run_id,
                    status=RunStatus.RUNNING,
                )
            except Exception as e:
                logger.error(f"Failed to create run task: {e}")
                raise

        if stream_events:
            yield handle_event(
                create_run_started_event(run_response),
                run_response,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
            )

        agent_session = await self._aread_or_create_session(session_id=session_id, user_id=user_id)

        run_context.session_state = self._initialize_session_state(
            session_state=run_context.session_state or {},
            user_id=user_id,
            session_id=session_id,
            run_id=run_response.run_id,
        )

        run_input = cast(RunInput, run_response.input)
        self.model = cast(Model, self.model)

        # Pre-hooks
        if self.pre_hooks is not None:
            async for event in self._hook_executor.execute_pre_hooks(
                hooks=self.pre_hooks,
                run_response=run_response,
                run_context=run_context,
                run_input=run_input,
                session=agent_session,
                agent_ref=self,
                user_id=user_id,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
                stream_events=stream_events,
                debug_mode=debug_mode,
                background_tasks=background_tasks,
                **kwargs,
            ):
                yield event

        # Determine tools
        processed_tools = await self._tool_manager.connect_and_get_tools(self.tools)
        delegate_func = self._get_delegate_func(run_response, run_context, agent_session, user_id, True, stream_events)
        _tools = self._tool_manager.determine_tools_for_model(
            processed_tools=processed_tools,
            tool_hooks=self.tool_hooks,
            run_response=run_response,
            run_context=run_context,
            session=agent_session,
            stream=True,
            stream_events=stream_events,
            user_id=user_id,
            agent_ref=self,
            delegate_func=delegate_func,
        )

        # Session summary
        if self.session_summary_manager is not None:
            should_summary = self.session_summary_manager.should_summary(agent_session)
            if should_summary:
                if stream_events:
                    yield handle_event(
                        create_session_summary_started_event(from_run_response=run_response),
                        run_response,
                        events_to_skip=self.events_to_skip,
                        store_events=self.store_events,
                    )
                try:
                    session_summary = await self.session_summary_manager.acreate_session_summary(
                        session=agent_session
                    )
                    if session_summary is not None:
                        run_response.summary = session_summary
                except Exception as e:
                    logger.warning(f"Error in session summary creation: {str(e)}")

                if stream_events:
                    yield handle_event(
                        create_session_summary_completed_event(
                            from_run_response=run_response,
                            session_summary=run_response.summary,
                        ),
                        run_response,
                        events_to_skip=self.events_to_skip,
                        store_events=self.store_events,
                    )

        if self.should_persist:
            await self.session_store.save_run(run_response)

        # Prepare messages
        run_messages = await self._message_builder.get_run_messages(
            run_response=run_response,
            run_context=run_context,
            input=run_input.input_content,
            session=agent_session,
            agent_id=self.id,
            user_id=user_id,
            audio=run_input.audios,
            images=run_input.images,
            videos=run_input.videos,
            files=run_input.files,
            tools=_tools,
            **kwargs,
        )
        if len(run_messages.messages) == 0:
            logger.error("No messages to be sent to the model.")

        try:
            async for event in self._response_handler.handle_model_response_stream(
                session=agent_session,
                run_response=run_response,
                run_messages=run_messages,
                tools=_tools,
                stream_events=stream_events,
                run_context=run_context,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
                agent_id=self.id,
                agent_name=self.name,
                sandbox_provider=self._sandbox_provider,
                tool_choice=self.tool_choice,
                tool_call_limit=self.tool_call_limit,
            ):
                await raise_if_cancelled(run_response.run_id)
                yield event

            await raise_if_cancelled(run_response.run_id)

            if stream_events:
                yield handle_event(
                    create_run_content_completed_event(from_run_response=run_response),
                    run_response,
                    events_to_skip=self.events_to_skip,
                    store_events=self.store_events,
                )

            # Post-hooks
            if self.post_hooks is not None:
                async for event in self._hook_executor.execute_post_hooks(
                    hooks=self.post_hooks,
                    run_output=run_response,
                    run_context=run_context,
                    session=agent_session,
                    agent_ref=self,
                    user_id=user_id,
                    events_to_skip=self.events_to_skip,
                    store_events=self.store_events,
                    stream_events=stream_events,
                    debug_mode=debug_mode,
                    background_tasks=background_tasks,
                    **kwargs,
                ):
                    yield event

            # HITL check
            if any(tool_call.is_paused for tool_call in run_response.tools or []):
                async for event in self._hitl_handler.handle_paused_stream(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                    cleanup_fn=self._acleanup_and_store,
                    events_to_skip=self.events_to_skip,
                    store_events=self.store_events,
                ):
                    yield event
                return

            if (
                agent_session.session_data is not None
                and "session_state" in agent_session.session_data
            ):
                run_response.session_state = agent_session.session_data["session_state"]

            run_response.status = RunStatus.COMPLETED
            completed_event = handle_event(
                create_run_completed_event(from_run_response=run_response),
                run_response,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
            )

            if stream_events:
                yield completed_event

            if yield_run_output:
                yield run_response

            logger.debug(f"Agent Run End: {run_response.run_id}")

        except RunCancelledException as e:
            logger.info(f"Run {run_response.run_id} was cancelled during async streaming")
            run_response.status = RunStatus.ABORTED
            self._response_handler.add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_abort_message(str(e)),
                is_error=False,
            )
            self._response_handler.finalize_run_response(
                run_response=run_response, run_messages=run_messages,
            )
            if not run_response.content:
                run_response.content = str(e)
            yield handle_event(
                create_run_cancelled_event(from_run_response=run_response, reason=str(e)),
                run_response,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
            )

        except Exception as e:
            logger.error(f"Run {run_response.run_id} failed with error: {e}")
            logger.error(f"Traceback:\n{traceback.format_exc()}")
            run_response.status = RunStatus.ERROR
            self._response_handler.add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_error_message(str(e)),
                is_error=True,
            )
            self._response_handler.finalize_run_response(
                run_response=run_response, run_messages=run_messages,
            )
            run_response.error_message = str(e)
            if not run_response.content:
                run_response.content = str(e)
            error_with_traceback = f"{str(e)}\n\nTraceback:\n{traceback.format_exc()}"
            yield handle_event(
                create_run_error_event(from_run_response=run_response, error=error_with_traceback),
                run_response,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
            )
            raise e

        finally:
            await self._tool_manager.disconnect_all()
            if run_response.status != RunStatus.PAUSED:
                await self._acleanup_and_store(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                )
            if not is_sub_agent:
                await cleanup_run(run_response.run_id)
            del agent_session

    async def _acontinue_run(
        self,
        run_id: str,
        run_context: RunContext,
        session_id: str,
        run_response: Optional[RunOutput] = None,
        updated_tools: Optional[List[ToolExecution]] = None,
        requirements: Optional[List[RunRequirement]] = None,
        user_id: Optional[str] = None,
        debug_mode: Optional[bool] = None,
        background_tasks: Optional[Any] = None,
        **kwargs: Any,
    ) -> RunOutput:
        """Continue a paused run (non-streaming)."""
        logger.debug(f"Agent Run Continue: {run_id}", center=True)

        agent_session = await self._aread_or_create_session(session_id=session_id, user_id=user_id)
        run_context.session_state = self._initialize_session_state(
            session_state={}, user_id=user_id, session_id=session_id, run_id=run_id
        )

        run_response = await self._prepare_continue_run_response(
            run_id=run_id, run_response=run_response, updated_tools=updated_tools,
            requirements=requirements, session_id=session_id,
        )
        run_response.status = RunStatus.RUNNING

        if self.should_persist:
            await self.session_store.update_run_status(
                run_id=run_response.run_id, status=run_response.status
            )

        self.model = cast(Model, self.model)
        processed_tools = await self._tool_manager.connect_and_get_tools(self.tools)
        _tools = self._tool_manager.determine_tools_for_model(
            processed_tools=processed_tools,
            tool_hooks=self.tool_hooks,
            run_response=run_response,
            run_context=run_context,
            session=agent_session,
            stream=False,
            stream_events=False,
            user_id=user_id,
            agent_ref=self,
        )

        run_messages = self._message_builder.get_continue_run_messages(
            input=run_response.messages or [],
        )

        await register_run(run_response.run_id)

        try:
            await self._hitl_handler.handle_tool_call_updates(
                run_response=run_response,
                run_messages=run_messages,
                tools=_tools,
                events_to_skip=self.events_to_skip,
                store_events=self.store_events,
            )

            model_response: ModelResponse = await self.model.aresponse(
                messages=run_messages.messages,
                tools=_tools,
                tool_choice=self.tool_choice,
                tool_call_limit=self.tool_call_limit,
            )
            await raise_if_cancelled(run_response.run_id)

            self._response_handler.update_run_response(
                model_response=model_response,
                run_response=run_response,
                run_messages=run_messages,
                run_context=run_context,
            )

            if any(tool_call.is_paused for tool_call in run_response.tools or []):
                return await self._hitl_handler.handle_paused(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                    cleanup_fn=self._acleanup_and_store,
                )

            if self.post_hooks is not None:
                async for _ in self._hook_executor.execute_post_hooks(
                    hooks=self.post_hooks,
                    run_output=run_response,
                    run_context=run_context,
                    session=agent_session,
                    agent_ref=self,
                    user_id=user_id,
                    events_to_skip=self.events_to_skip,
                    store_events=self.store_events,
                    debug_mode=debug_mode,
                    background_tasks=background_tasks,
                    **kwargs,
                ):
                    pass

            if self.session_summary_manager is not None and not run_response.is_sub_agent_response:
                await self.session_store.save_run(run=run_response)
                try:
                    await self.session_summary_manager.acreate_session_summary(
                        session=agent_session
                    )
                except Exception as e:
                    logger.warning(f"Error in session summary creation: {str(e)}")

            if (
                agent_session.session_data is not None
                and "session_state" in agent_session.session_data
            ):
                run_response.session_state = agent_session.session_data["session_state"]

            run_response.status = RunStatus.COMPLETED
            logger.debug(f"Agent Run End: {run_response.run_id}", center=True, symbol="*")
            return run_response

        except RunCancelledException as e:
            logger.info(f"Run {run_response.run_id} was cancelled")
            run_response.status = RunStatus.ABORTED
            self._response_handler.add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_abort_message(str(e)),
                is_error=False,
            )
            self._response_handler.finalize_run_response(
                run_response=run_response, run_messages=run_messages,
            )
            if not run_response.content:
                run_response.content = str(e)
            return run_response

        except Exception as e:
            logger.error(f"Run {run_response.run_id} failed with error: {e}")
            logger.error(f"Traceback:\n{traceback.format_exc()}")
            run_response.status = RunStatus.ERROR
            self._response_handler.add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_error_message(str(e)),
                is_error=True,
            )
            self._response_handler.finalize_run_response(
                run_response=run_response, run_messages=run_messages,
            )
            run_response.error_message = str(e)
            if not run_response.content:
                run_response.content = str(e)
            raise

        finally:
            await self._tool_manager.disconnect_all()
            if run_response.status != RunStatus.PAUSED:
                await self._acleanup_and_store(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                )
            await cleanup_run(run_response.run_id)

