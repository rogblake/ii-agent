@dataclass
class IIAgent:
    user_id: str
    session_id: str
    model: Model
    name: str = None

    _internal_lock: asyncio.Lock = field(default_factory=asyncio.Lock, init=False, repr=False)
    _sandbox: Optional[Sandbox] = None
    _mcp_tools_initialized_on_run: Optional[List[Any]] = None
    _connectable_tools_initialized_on_run: Optional[List[Any]] = None
    _sandbox_was_initialized: Optional[bool] = None
    # Agent UUID (autogenerated if not set)
    id: Optional[str] = None

    session_store: Optional[SessionStore] = None
    # Default session state (stored in the database to persist across runs)
    session_state: Optional[Dict[str, Any]] = None

    # Session summary manager
    session_summary_manager: Optional[SessionSummaryManager] = None

    # --- Agent Tools ---
    # A list of tools provided to the Model.
    # Tools are functions the model may generate JSON inputs for.
    tools: Optional[List[Union[BaseAgentTool, Toolkit, Callable, Function, Dict]]] = None

    # Maximum number of tool calls allowed.
    tool_call_limit: Optional[int] = None

    tool_choice: Optional[Union[str, Dict[str, Any]]] = None

    # A function that acts as middleware and is called around tool calls.
    tool_hooks: Optional[List[Callable]] = None

    # --- Agent Hooks ---
    # Functions called right after agent-session is loaded, before processing starts
    pre_hooks: Optional[List[Union[Callable[..., Any]]]] = None
    # Functions called after output is generated but before the response is returned
    post_hooks: Optional[List[Union[Callable[..., Any]]]] = None
    # Provide the system message as a string or function (takes precedence over prompt_config)
    system_message: Optional[Union[str, Callable, Message]] = None
    # The role of the system message (default: "system")
    # --- Settings for building the default system message (legacy, use prompt_config instead) ---
    # A description of the Agent that is added to the start of the system message.
    description: Optional[str] = None
    # List of instructions for the agent.
    instructions: Optional[Union[str, List[str], Callable]] = None
    additional_context: Optional[str] = None
    # add_datetime_to_context: bool = False

    # --- Agent Response Settings ---
    # Number of retries to attempt
    retries: int = 0
    # Delay between retries (in seconds)
    delay_between_retries: int = 1
    # Exponential backoff: if True, the delay between retries is doubled each time
    exponential_backoff: bool = False

    # --- Agent Streaming ---
    # Stream the response from the Agent
    stream: Optional[bool] = None
    # Stream the intermediate steps from the Agent
    stream_events: Optional[bool] = None

    # Persist the events on the run response
    store_events: bool = False
    events_to_skip: Optional[List[RunEvent]] = None

    # Metadata stored with this agent
    metadata: Optional[Dict[str, Any]] = None

    # --- Sub-Agent Support (Team-like functionality) ---
    # List of sub-agents that can be delegated tasks
    sub_agents: Optional[List["IIAgent"]] = None
    # If True, delegate to all sub-agents; otherwise delegate to specific member
    delegate_to_all_members: bool = False
    # Stream sub-agent events to parent agent stream
    stream_member_events: bool = True
    # Store sub-agent responses in parent's member_responses
    store_member_responses: bool = False
    # Role description for this agent (used when it's a sub-agent)
    role: Optional[str] = None

    def __post_init__(self) -> None:
        # Ensure tools is a list
        if self.tools is not None:
            self.tools = list(self.tools)
        else:
            self.tools = []

        # Default events_to_skip
        if self.events_to_skip is None:
            self.events_to_skip = [
                RunContentDeltaEvent,
                ReasoningDeltaEvent,
            ]
        if self._connectable_tools_initialized_on_run is None:
            self._connectable_tools_initialized_on_run = []
        if self._mcp_tools_initialized_on_run is None:
            self._mcp_tools_initialized_on_run = []
        if self.session_store is None:
            self.session_store = NoOpSessionStore()
        if self.session_summary_manager is None:
            self._set_session_summary_manager()
        # Initialize sub-agents
        if self.sub_agents is None:
            self.sub_agents = []
        else:
            # Initialize each sub-agent with shared context
            for sub_agent in self.sub_agents:
                self._initialize_sub_agent(sub_agent)

    def _initialize_sub_agent(self, sub_agent: "IIAgent") -> None:
        """Initialize a sub-agent with shared context from parent."""
        # Share session store if not set
        if sub_agent.session_store is None or isinstance(sub_agent.session_store, NoOpSessionStore):
            sub_agent.session_store = self.session_store

    def add_sub_agent(self, agent: "IIAgent") -> None:
        """Add a sub-agent to this agent."""
        if self.sub_agents is None:
            self.sub_agents = []
        self._initialize_sub_agent(agent)
        self.sub_agents.append(agent)

    def _find_sub_agent_by_id(self, member_id: str) -> Optional["IIAgent"]:
        """Find a sub-agent by ID or name."""
        if not self.sub_agents:
            return None

        for agent in self.sub_agents:
            if agent.id == member_id or agent.name == member_id:
                return agent
        return None

    def get_sub_agents_description(self) -> str:
        """Get formatted description of sub-agents for system prompt."""
        if not self.sub_agents:
            return ""

        lines = ["## Available Sub-Agents\n"]
        for agent in self.sub_agents:
            agent_id = agent.id or agent.name
            lines.append(f"### {agent.name} (ID: {agent_id})")
            if agent.role:
                lines.append(f"**Role**: {agent.role}")
            if agent.description:
                lines.append(f"**Description**: {agent.description}")
            lines.append("")

        return "\n".join(lines)

    def _get_delegate_task_function(
        self,
        run_response: RunOutput,
        run_context: RunContext,
        session: "AgentSession",
        user_id: Optional[str] = None,
        stream: bool = False,
        stream_events: bool = False,
        **kwargs: Any,
    ) -> Function:
        """Create delegation function as a tool for the model.

        This follows the agno Team pattern where delegation is implemented
        as a callable function that internally calls agent.arun().
        """
        # Capture context for closure
        parent_agent = self
        parent_run_id = run_response.run_id
        parent_session_id = session.session_id

        async def adelegate_task_to_member(
            member_id: str,
            task: str,
        ) -> AsyncIterator[Union[RunOutputEvent, RunOutput]]:
            """
            Delegate a task to a specific sub-agent.

            Args:
                member_id: The ID or name of the sub-agent to delegate to
                task: The task description/prompt for the sub-agent

            Returns:
                The sub-agent's response content
            """
            # Find the sub-agent
            sub_agent = parent_agent._find_sub_agent_by_id(member_id)
            if sub_agent is None:
                available = [a.id or a.name for a in parent_agent.sub_agents or []]
                yield create_run_error_event(
                    from_run_response=run_response,
                    error=f"Sub-agent with ID '{member_id}' not found. Available: {available}",
                )
                return

            member_session_state = dict(run_context.session_state or {})

            # Sub-agents use parent's run_id for unified cancellation handling
            if stream:
                sub_agent_stream = await sub_agent.arun(
                    run_id=parent_run_id,
                    input=task,
                    user_id=user_id,
                    session_id=parent_session_id,
                    session_state=member_session_state,
                    stream=True,
                    stream_events=stream_events or parent_agent.stream_member_events,
                    yield_run_output=True,
                    is_sub_agent=True,
                    **kwargs,
                )

                async for event in sub_agent_stream:
                    event.parent_run_id = parent_run_id
                    event.delegated_from = parent_agent.name
                    if isinstance(event, RunOutput):
                        # Process RunOutput internally but don't yield to main stream
                        # Content was already sent via RunContentEvent during streaming
                        if event.status == RunStatus.CANCELLED:
                            await raise_if_cancelled(parent_run_id)

                        if parent_agent.store_member_responses:
                            run_response.add_member_run(event)

                        if event.session_state:
                            merge_dictionaries(
                                run_context.session_state or {},
                                event.session_state,
                            )
                        # Don't yield RunOutput - it causes duplicate message in frontend
                        continue
                    else:
                        event.is_sub_agent_event = True

                    yield event

                await raise_if_cancelled(parent_run_id)
            else:
                sub_agent_response = await sub_agent.arun(
                    run_id=parent_run_id,
                    input=task,
                    user_id=user_id,
                    session_id=parent_session_id,
                    session_state=member_session_state,
                    stream=False,
                    is_sub_agent=True,
                    **kwargs,
                )
                sub_agent_response = cast(RunOutput, sub_agent_response)

                await raise_if_cancelled(parent_run_id)

                if sub_agent_response.status == RunStatus.CANCELLED:
                    await raise_if_cancelled(parent_run_id)

                sub_agent_response.parent_run_id = parent_run_id
                sub_agent_response.delegated_from = parent_agent.name

                if parent_agent.store_member_responses:
                    run_response.add_member_run(sub_agent_response)

                yield sub_agent_response

        async def adelegate_task_to_all_members(
            task: str,
        ) -> AsyncIterator[Union[RunOutputEvent, RunOutput]]:
            """
            Delegate a task to ALL sub-agents sequentially.

            Args:
                task: The task description/prompt for all sub-agents

            Returns:
                Combined responses from all sub-agents
            """
            for sub_agent in parent_agent.sub_agents or []:
                member_id = sub_agent.id or sub_agent.name
                async for event in adelegate_task_to_member(member_id, task):
                    yield event

        # Build member list for description
        sub_agents_description = self.get_sub_agents_description()
        # Choose delegation function based on settings
        # Tool name must start with "sub_agent" for frontend to recognize it
        # Name matches what system prompt tells model to use (sub_agent_task)
        if self.delegate_to_all_members:
            delegate_func = Function.from_callable(
                adelegate_task_to_all_members,
                name="sub_agent_task_all",
            )
            delegate_func.description = (
                "Delegate a task to ALL sub-agents and get combined results. All available sub-agents:\n"
                f"{sub_agents_description}"
            )
        else:
            delegate_func = Function.from_callable(
                adelegate_task_to_member,
                name="sub_agent_task",
            )
            delegate_func.description = (
                f"Delegate a task to a specific sub-agent. Available sub-agents:\n{sub_agents_description}"
            )

        delegate_func.stop_after_tool_call = False
        delegate_func.show_result = True

        return delegate_func

    def set_id(self) -> None:
        if self.id is None:
            self.id = generate_id_from_name(self.name)

    @property
    def should_persist(self) -> bool:
        """Check if session store is available for persistence."""
        from ii_agent.agents.sessions.base import NoOpSessionStore

        return self.session_store is not None and not isinstance(
            self.session_store, NoOpSessionStore
        )

    def _validate_input(
        self, input: Union[str, List, Dict, Message, BaseModel]
    ) -> Union[str, List, Dict, Message, BaseModel]:
        """Parse and validate input, returning input as-is"""
        return input

    @property
    def sandbox(self) -> Optional[Sandbox]:
        """Get the sandbox (may not be initialized yet)."""
        return self._sandbox

    @sandbox.setter
    def sandbox(self, value: Optional[Sandbox]) -> None:
        """Set the sandbox."""
        self._sandbox = value

    def _set_session_summary_manager(self) -> None:
        if self.session_summary_manager is None:
            self.session_summary_manager = SessionSummaryManager(model=self.model)

        if self.session_summary_manager is not None:
            if self.session_summary_manager.model is None:
                self.session_summary_manager.model = self.model

    def add_tool(self, tool: Union[Toolkit, Callable, Function, Dict, BaseAgentTool]):
        if not self.tools:
            self.tools = []
        self.tools.append(tool)

    def set_tools(self, tools: Sequence[Union[Toolkit, Callable, Function, Dict, BaseAgentTool]]):
        self.tools = list(tools) if tools else []

    async def _connect_mcp_tools(self) -> None:
        """Connect the MCP tools to the agent."""
        if self.tools:
            for tool in self.tools:
                # Alternate method of using isinstance(tool, (MCPTools, MultiMCPTools)) to avoid imports
                if (
                    hasattr(type(tool), "__mro__")
                    and any(c.__name__ in ["MCPTools", "MultiMCPTools"] for c in type(tool).__mro__)
                    and not tool.initialized  # type: ignore
                ):
                    try:
                        # Connect the MCP server
                        await tool.connect()  # type: ignore
                        self._mcp_tools_initialized_on_run.append(tool)  # type: ignore
                    except Exception as e:
                        logger.warning(f"Error connecting tool: {str(e)}")

    async def _disconnect_mcp_tools(self) -> None:
        """Disconnect the MCP tools from the agent."""
        for tool in self._mcp_tools_initialized_on_run:
            try:
                await tool.close()
            except Exception as e:
                logger.warning(f"Error disconnecting tool: {str(e)}")
        self._mcp_tools_initialized_on_run = []

    def _connect_connectable_tools(self) -> None:
        """Connect tools that require connection management (e.g., database connections)."""
        if self.tools:
            for tool in self.tools:
                if (
                    hasattr(tool, "requires_connect")
                    and tool.requires_connect
                    and hasattr(tool, "connect")
                    and tool not in self._connectable_tools_initialized_on_run
                ):
                    try:
                        tool.connect()  # type: ignore
                        self._connectable_tools_initialized_on_run.append(tool)
                    except Exception as e:
                        logger.warning(f"Error connecting tool: {str(e)}")

    def _disconnect_connectable_tools(self) -> None:
        """Disconnect tools that require connection management."""
        for tool in self._connectable_tools_initialized_on_run:
            if hasattr(tool, "close"):
                try:
                    tool.close()  # type: ignore
                except Exception as e:
                    logger.warning(f"Error disconnecting tool: {str(e)}")
        self._connectable_tools_initialized_on_run = []

    def _initialize_session_state(
        self,
        session_state: Dict[str, Any],
        user_id: Optional[str] = None,
        session_id: Optional[str] = None,
        run_id: Optional[str] = None,
    ) -> Dict[str, Any]:
        """Initialize the session state for the agent."""
        if user_id:
            session_state["current_user_id"] = user_id
        if session_id is not None:
            session_state["current_session_id"] = session_id
        if run_id is not None:
            session_state["current_run_id"] = run_id
        return session_state

    async def _arun(
        self,
        run_response: RunOutput,
        run_context: RunContext,
        session_id: str,
        user_id: Optional[str] = None,
        debug_mode: Optional[bool] = None,
        background_tasks: Optional[Any] = None,
        is_sub_agent: bool = False,
        **kwargs: Any,
    ) -> RunOutput:
        """Run the Agent and return the RunOutput.

        Steps:
        0. Create run task in database (if session_store available)
        1. Read or create session
        2. Update metadata and session state
        3. Resolve dependencies
        4. Execute pre-hooks
        5. Determine tools for model
        6. Prepare run messages
        7. Start memory creation in background task
        8. Reason about the task if reasoning is enabled
        9. Generate a response from the Model (includes running function calls)
        10. Update the RunOutput with the model response
        11. Convert response to structured format
        12. Store media if enabled
        13. Execute post-hooks
        14. Wait for background memory creation
        15. Create session summary
        16. Cleanup and store (scrub, stop timer, save to file, add to session, calculate metrics, save session)
        """
        logger.debug(f"Agent Run Start: {run_response.run_id}")

        # Create run task for main agent only (sub-agents share parent's run_id for cancellation)
        if self.should_persist and not is_sub_agent:
            try:
                await self.session_store.get_or_create_run_task(
                    session_id=session_id,
                    run_id=run_response.run_id,
                    status=RunStatus.RUNNING,
                )
                logger.debug(f"Created run task {run_response.run_id} in database")
            except Exception as e:
                logger.error(f"Failed to create run task: {e}")
                raise

        # 1. Read or create session. Reads from the database if provided.
        agent_session: AgentSession = await self._aread_or_create_session(
            session_id=session_id, user_id=user_id
        )

        # 2. Initialize session state
        run_context.session_state = self._initialize_session_state(
            session_state=run_context.session_state
            if run_context.session_state is not None
            else {},
            user_id=user_id,
            session_id=session_id,
            run_id=run_response.run_id,
        )

        # 4. Execute pre-hooks
        run_input = cast(RunInput, run_response.input)
        self.model = cast(Model, self.model)
        if self.pre_hooks is not None:
            # Can modify the run input
            pre_hook_iterator = self._aexecute_pre_hooks(
                hooks=self.pre_hooks,  # type: ignore
                run_response=run_response,
                run_context=run_context,
                run_input=run_input,
                session=agent_session,
                user_id=user_id,
                debug_mode=debug_mode,
                background_tasks=background_tasks,
                **kwargs,
            )
            # Consume the async iterator without yielding
            async for _ in pre_hook_iterator:
                pass

        # 5. Determine tools for model
        self.model = cast(Model, self.model)
        processed_tools = await self.aget_tools()

        _tools = self._determine_tools_for_model(
            model=self.model,
            processed_tools=processed_tools,
            run_response=run_response,
            run_context=run_context,
            session=agent_session,
            stream=False,
            stream_events=False,
            user_id=user_id,
        )

        # 5.5 Create session summary at beginning of run if threshold is met
        if self.session_summary_manager is not None:
            should_summary = self.session_summary_manager.should_summary(agent_session)
            if should_summary:
                try:
                    session_summary = await self.session_summary_manager.acreate_session_summary(
                        session=agent_session
                    )
                    if session_summary is not None:
                        run_response.summary = session_summary
                        logger.debug("Created session summary at beginning of run")
                except Exception as e:
                    logger.warning(f"Error in session summary creation: {str(e)}")

        if self.should_persist:
            # Save the run before starting model processing
            self.session_store.save_run(run_response)
        # 6. Prepare run messages
        run_messages: RunMessages = await self._aget_run_messages(
            run_response=run_response,
            run_context=run_context,
            input=run_input.input_content,
            session=agent_session,
            user_id=user_id,
            audio=run_input.audios,
            images=run_input.images,
            videos=run_input.videos,
            files=run_input.files,
            tools=_tools,
            **kwargs,
        )
        if len(run_messages.messages) == 0:
            logger.error("No messages to be sent to the model.")

        try:
            # Check for cancellation before model call
            await raise_if_cancelled(run_response.run_id)  # type: ignore

            # 9. Generate a response from the Model (includes running function calls)
            model_response: ModelResponse = await self.model.aresponse(
                messages=run_messages.messages,
                tools=_tools,
                tool_choice=self.tool_choice,
                tool_call_limit=self.tool_call_limit,
                run_response=run_response,
            )

            # Check for cancellation after model call
            await raise_if_cancelled(run_response.run_id)  # type: ignore

            # 10. Update the RunOutput with the model response
            self._update_run_response(
                model_response=model_response,
                run_response=run_response,
                run_messages=run_messages,
                run_context=run_context,
            )

            store_media_util(run_response, model_response)

            # 13. Execute post-hooks (after output is generated but before response is returned)
            if self.post_hooks is not None:
                async for _ in self._aexecute_post_hooks(
                    hooks=self.post_hooks,  # type: ignore
                    run_output=run_response,
                    run_context=run_context,
                    session=agent_session,
                    user_id=user_id,
                    debug_mode=debug_mode,
                    background_tasks=background_tasks,
                    **kwargs,
                ):
                    pass

            # Check for cancellation
            await raise_if_cancelled(run_response.run_id)  # type: ignore

            # Check if run has unresolved requirements (HITL - Human in the Loop)
            # If requirements exist, pause the run and wait for user input
            if any(tool_call.is_paused for tool_call in run_response.tools or []):
                return await self._ahandle_agent_run_paused(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                )

            run_response.status = RunStatus.COMPLETED
            logger.debug(f"Agent Run End: {run_response.run_id}")
            return run_response

        except RunCancelledException as e:
            # Handle run cancellation
            logger.info(f"Run {run_response.run_id} was cancelled")
            run_response.status = RunStatus.CANCELLED

            # Add fake tool results for any pending tool calls to prevent next turn errors
            self._add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_abort_message(str(e)),
                is_error=False,
            )

            self._finalize_run_response(
                run_response=run_response,
                run_messages=run_messages,
            )
            if not run_response.content:
                run_response.content = str(e)
            return run_response

        except Exception as e:
            # Handle any unhandled exception
            logger.error(f"Run {run_response.run_id} failed with error: {e}")
            tb = traceback.format_exc()
            logger.error(f"Traceback:\n{tb}")
            run_response.status = RunStatus.FAILED

            # Add fake tool results for any pending tool calls to prevent next turn errors
            self._add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_error_message(str(e)),
                is_error=True,
            )

            self._finalize_run_response(
                run_response=run_response,
                run_messages=run_messages,
            )
            run_response.error_message = tb[:500]
            if not run_response.content:
                run_response.content = str(e)
            raise

        finally:
            self._disconnect_connectable_tools()
            await self._disconnect_mcp_tools()

            if run_response.status != RunStatus.PAUSED:
                await self._acleanup_and_store(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                )

            if not is_sub_agent:
                await cleanup_run(run_response.run_id)

    async def _arun_stream(
        self,
        run_response: RunOutput,
        run_context: RunContext,
        session_id: str,
        user_id: Optional[str] = None,
        add_history_to_context: Optional[bool] = None,
        add_dependencies_to_context: Optional[bool] = None,
        add_session_state_to_context: Optional[bool] = None,
        stream_events: bool = False,
        yield_run_output: Optional[bool] = None,
        debug_mode: Optional[bool] = None,
        background_tasks: Optional[Any] = None,
        is_sub_agent: bool = False,
        **kwargs: Any,
    ) -> AsyncIterator[Union[RunOutputEvent, RunOutput]]:
        """Run the Agent and yield the RunOutput.

        Steps:
        0. Create run task in database (if session_store available)
        1. Read or create session
        2. Update metadata and session state
        3. Resolve dependencies
        4. Execute pre-hooks
        5. Determine tools for model
        6. Prepare run messages
        7. Start memory creation in background task
        8. Reason about the task if reasoning is enabled
        9. Generate a response from the Model (includes running function calls)
        10. Parse response with parser model if provided
        11. Wait for background memory creation
        12. Create session summary
        13. Cleanup and store (scrub, stop timer, save to file, add to session, calculate metrics, save session)
        """
        logger.debug(f"Agent Run Start: {run_response.run_id}", center=True)

        # Create run task for main agent only (sub-agents share parent's run_id for cancellation)
        if self.should_persist and not is_sub_agent:
            try:
                await self.session_store.get_or_create_run_task(
                    session_id=session_id,
                    run_id=run_response.run_id,
                    status=RunStatus.RUNNING,
                )
                logger.debug(f"Created run task {run_response.run_id} in database")
            except Exception as e:
                logger.error(f"Failed to create run task: {e}")
                raise
        if stream_events:
            yield handle_event(  # type: ignore
                create_run_started_event(run_response),
                run_response,
                events_to_skip=self.events_to_skip,  # type: ignore
                store_events=self.store_events,
            )

        # 1. Read or create session. Reads from the database if provided.
        agent_session = await self._aread_or_create_session(session_id=session_id, user_id=user_id)

        # 2. Initialize session state
        run_context.session_state = self._initialize_session_state(
            session_state=run_context.session_state
            if run_context.session_state is not None
            else {},
            user_id=user_id,
            session_id=session_id,
            run_id=run_response.run_id,
        )

        # 4. Execute pre-hooks
        run_input = cast(RunInput, run_response.input)
        self.model = cast(Model, self.model)
        if self.pre_hooks is not None:
            # Can modify the run input
            pre_hook_iterator = self._aexecute_pre_hooks(
                hooks=self.pre_hooks,  # type: ignore
                run_response=run_response,
                run_context=run_context,
                run_input=run_input,
                session=agent_session,
                user_id=user_id,
                debug_mode=debug_mode,
                stream_events=stream_events,
                background_tasks=background_tasks,
                **kwargs,
            )
            async for event in pre_hook_iterator:
                yield event

        # 5. Determine tools for model
        self.model = cast(Model, self.model)
        processed_tools = await self.aget_tools()

        _tools = self._determine_tools_for_model(
            model=self.model,
            processed_tools=processed_tools,
            run_response=run_response,
            run_context=run_context,
            session=agent_session,
            stream=True,
            stream_events=stream_events,
            user_id=user_id,
        )

        # 5.5 Create session summary at beginning of run if threshold is met
        if self.session_summary_manager is not None:
            should_summary = self.session_summary_manager.should_summary(agent_session)
            if should_summary:
                if stream_events:
                    yield handle_event(  # type: ignore
                        create_session_summary_started_event(from_run_response=run_response),
                        run_response,
                        events_to_skip=self.events_to_skip,  # type: ignore
                        store_events=self.store_events,
                    )
                try:
                    session_summary = await self.session_summary_manager.acreate_session_summary(
                        session=agent_session
                    )
                    if session_summary is not None:
                        # new summary created and attach to run response
                        run_response.summary = session_summary
                        logger.debug("Created session summary at beginning of run")
                except Exception as e:
                    logger.warning(f"Error in session summary creation: {str(e)}")

                if stream_events:
                    yield handle_event(  # type: ignore
                        create_session_summary_completed_event(
                            from_run_response=run_response,
                            session_summary=run_response.summary,
                        ),
                        run_response,
                        events_to_skip=self.events_to_skip,  # type: ignore
                        store_events=self.store_events,
                    )

        if self.should_persist:
            # Save the run before starting model processing
            await self.session_store.save_run(run_response)
        # 6. Prepare run messages
        run_messages: RunMessages = await self._aget_run_messages(
            run_response=run_response,
            run_context=run_context,
            input=run_input.input_content,
            session=agent_session,
            user_id=user_id,
            audio=run_input.audios,
            images=run_input.images,
            videos=run_input.videos,
            files=run_input.files,
            add_history_to_context=add_history_to_context,
            add_dependencies_to_context=add_dependencies_to_context,
            add_session_state_to_context=add_session_state_to_context,
            tools=_tools,
            **kwargs,
        )
        if len(run_messages.messages) == 0:
            logger.error("No messages to be sent to the model.")
        try:
            cancelled_exc: RunCancelledException | None = None
            model_stream = self._ahandle_model_response_stream(
                session=agent_session,
                run_response=run_response,
                run_messages=run_messages,
                tools=_tools,
                stream_events=stream_events,
                run_context=run_context,
            )
            try:
                async for event in model_stream:
                    await raise_if_cancelled(run_response.run_id)  # type: ignore
                    yield event
            except RunCancelledException as cancel_err:
                # Catch cancellation here so we can close the inner generator
                # cleanly before re-raising.  Letting the exception propagate
                # directly causes Python to call aclose() on the inner
                # generator while it is still running, which triggers
                # "RuntimeError: aclose(): asynchronous generator is already
                # running".
                cancelled_exc = cancel_err
            finally:
                await model_stream.aclose()

            if cancelled_exc is not None:
                raise cancelled_exc

            # Check for cancellation after model processing
            await raise_if_cancelled(run_response.run_id)  # type: ignore

            if stream_events:
                yield handle_event(  # type: ignore
                    create_run_content_completed_event(from_run_response=run_response),
                    run_response,
                    events_to_skip=self.events_to_skip,  # type: ignore
                    store_events=self.store_events,
                )

            # Execute post-hooks (after output is generated but before response is returned)
            if self.post_hooks is not None:
                async for event in self._aexecute_post_hooks(
                    hooks=self.post_hooks,  # type: ignore
                    run_output=run_response,
                    run_context=run_context,
                    session=agent_session,
                    user_id=user_id,
                    debug_mode=debug_mode,
                    stream_events=stream_events,
                    background_tasks=background_tasks,
                    **kwargs,
                ):
                    yield event

            # Check if run has unresolved requirements (HITL - Human in the Loop)
            # If requirements exist, pause the run and wait for user input
            if any(tool_call.is_paused for tool_call in run_response.tools or []):
                async for event in self._ahandle_agent_run_paused_stream(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                ):
                    yield event
                return  # Stop execution here, run is paused

            # Update run_response.session_state before creating RunCompletedEvent
            # This ensures the event has the final state after all tool modifications
            if (
                agent_session.session_data is not None
                and "session_state" in agent_session.session_data
            ):
                run_response.session_state = agent_session.session_data["session_state"]
            # Set the run status to completed
            run_response.status = RunStatus.COMPLETED
            # Create the run completed event
            completed_event = handle_event(
                create_run_completed_event(from_run_response=run_response),
                run_response,
                events_to_skip=self.events_to_skip,  # type: ignore
                store_events=self.store_events,
            )

            if stream_events:
                yield completed_event  # type: ignore

            if yield_run_output:
                yield run_response

            logger.debug(f"Agent Run End: {run_response.run_id}")

        except RunCancelledException as e:
            # Handle run cancellation during async streaming
            logger.info(f"Run {run_response.run_id} was cancelled during async streaming")
            run_response.status = RunStatus.CANCELLED

            # Add fake tool results for any pending tool calls to prevent next turn errors
            self._add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_abort_message(str(e)),
                is_error=False,
            )

            self._finalize_run_response(
                run_response=run_response,
                run_messages=run_messages,
            )
            if not run_response.content:
                run_response.content = str(e)

            # Yield the cancellation event
            yield handle_event(  # type: ignore
                create_run_cancelled_event(from_run_response=run_response, reason=str(e)),
                run_response,
                events_to_skip=self.events_to_skip,  # type: ignore
                store_events=self.store_events,
            )

        except Exception as e:
            # Handle any unhandled exception
            logger.error(f"Run {run_response.run_id} failed with error: {e}")
            logger.error(f"Traceback:\n{traceback.format_exc()}")
            run_response.status = RunStatus.FAILED

            # Add fake tool results for any pending tool calls to prevent next turn errors
            self._add_fake_tool_results_for_pending_calls(
                run_messages=run_messages,
                error_message=get_tool_error_message(str(e)),
                is_error=True,
            )

            self._finalize_run_response(
                run_response=run_response,
                run_messages=run_messages,
            )
            run_response.error_message = str(e)
            if not run_response.content:
                run_response.content = str(e)

            # Yield the error event before re-raising (include full traceback)
            error_with_traceback = f"{str(e)}\n\nTraceback:\n{traceback.format_exc()}"
            yield handle_event(  # type: ignore
                create_run_error_event(from_run_response=run_response, error=error_with_traceback),
                run_response,
                events_to_skip=self.events_to_skip,  # type: ignore
                store_events=self.store_events,
            )
            raise e

        finally:
            self._disconnect_connectable_tools()
            await self._disconnect_mcp_tools()

            if run_response.status != RunStatus.PAUSED:
                await self._acleanup_and_store(
                    run_response=run_response,
                    session=agent_session,
                    run_context=run_context,
                )

            if not is_sub_agent:
                await cleanup_run(run_response.run_id)
            # Free memory - session data is persisted, delete reference to allow GC
            del agent_session

