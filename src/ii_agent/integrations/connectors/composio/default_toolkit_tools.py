def get_default_tools(toolkit_slug: str) -> List[str]:
    """Get default enabled tools for a toolkit.
    
    Args:
        toolkit_slug: The toolkit identifier (e.g., "googledrive", "gmail")
        
    Returns:
        List of default tool slugs for the toolkit.
        Returns empty list if no defaults defined.
    """
    return DEFAULT_TOOLKIT_TOOLS.get(toolkit_slug.lower(), [])


def has_default_tools(toolkit_slug: str) -> bool:
    """Check if a toolkit has default tools configured.
    
    Args:
        toolkit_slug: The toolkit identifier
        
    Returns:
        True if toolkit has default tools defined
    """
    return toolkit_slug.lower() in DEFAULT_TOOLKIT_TOOLS


def get_all_toolkit_slugs_with_defaults() -> List[str]:
    """Get list of all toolkit slugs that have default tools configured.
    
    Returns:
        List of toolkit slugs
    """
    return list(DEFAULT_TOOLKIT_TOOLS.keys())
